def _col(name, type_="int", nullable=True, default=None, **kw):
    return ColumnInfo(name=name, type=type_, nullable=nullable, default=default, **kw)


def _tbl(name, columns, *, pk=None, fks=None, indexes=None, uniques=None,
         checks=None, storage=None, comment=None, database="db"):
    return TableSchema(
        database=database, table=name, columns=columns, primary_key=pk or [],
        foreign_keys=fks or [], indexes=indexes or [], unique_constraints=uniques or [],
        check_constraints=checks or [], storage_options=storage or {}, comment=comment,
    )


def _snap(engine="mysql", *, tables=None, views=None, routines=None, triggers=None,
          sequences=None, enum_types=None, extensions=None, events=None):
    return SchemaSnapshot(
        database="db", source_engine=engine, tables=tables or [], views=views or [],
        routines=routines or [], triggers=triggers or [], sequences=sequences or [],
        enum_types=enum_types or [], extensions=extensions or [], events=events or [],
    )


def _items_by(diff, object_type=None, change_type=None):
    out = diff.items
    if object_type is not None:
        out = [i for i in out if i.object_type == object_type]
    if change_type is not None:
        out = [i for i in out if i.change_type == change_type]
    return out


