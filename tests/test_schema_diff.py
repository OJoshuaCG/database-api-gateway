def test_table_rename_heuristic_marks_both_items():
    cols = [_col("id", "int"), _col("name", "varchar(50)"), _col("email", "varchar(80)")]
    src = _snap("mysql", tables=[_tbl("clients", cols)])   # nueva
    tgt = _snap("mysql", tables=[_tbl("customers", cols)])  # eliminada (misma firma)
    diff = sd.diff_snapshots(src, tgt)
    new = _items_by(diff, "table", "new")[0]
    drop = _items_by(diff, "table", "dropped")[0]
    assert new.risk.possible_rename_of == "customers"
    assert drop.risk.possible_rename_of == "clients"
    # possible_rename => destructivo (fail-closed): el DROP nunca se oculta.
    assert new.risk.destructive is True
    assert drop.risk.destructive is True


